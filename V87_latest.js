        // 強化填入（contenteditable + textarea 都支援）
        function fillInputBox(box, content) {
            box.focus();
            const isEditable = box.contentEditable === 'true' || box.hasAttribute('contenteditable');
            if (isEditable) {
                box.innerText = '';
                box.focus();
                document.execCommand('selectAll', false, null);
                document.execCommand('insertText', false, content);
                if (!box.innerText.trim()) {
                    box.innerText = content;
                    box.dispatchEvent(new InputEvent('input', { bubbles: true, data: content }));
                }
            } else {
                const lastValue = box.value;
                const setter = Object.getOwnPropertyDescriptor(window.HTMLTextAreaElement.prototype, 'value')?.set
                    || Object.getOwnPropertyDescriptor(window.HTMLInputElement.prototype, 'value')?.set;
                if (setter) setter.call(box, content);
                else box.value = content;
                if (box._valueTracker) box._valueTracker.setValue(lastValue);
                box.dispatchEvent(new Event('input', { bubbles: true }));
                box.dispatchEvent(new Event('change', { bubbles: true }));
            }
            box.scrollTop = box.scrollHeight;
        }

