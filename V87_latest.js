    async function buildTrilingualReport(items, isTodayFlag) {
        const results = [];
        for (let i = 0; i < items.length; i++) {
            const item = items[i];
            const zh = await translateText(item.content, 'zh-TW');
            const en = await translateText(item.content, 'en');
            const prefix = `🔹 【最新進度 ${i + 1}】\n`;
            const senderLine = item.sender ? `🏢 ${item.sender}\n` : '';
            const dateLine = item.date ? `📅 ${item.date}\n` : '';
            results.push({
                orig: `${prefix}${senderLine}${dateLine}💬 ${item.content}`,
                zh:   `${prefix}${senderLine}${dateLine}💬 ${zh}`,
                en:   `${prefix}${senderLine}${dateLine}💬 ${en}`
            });
        }
        const statusZH = isTodayFlag ? '✅ 【今日管理公司最新回覆】' : 'ℹ️ 【今日暫無新訊息，此為目前的最新進度】';
        const statusEN = isTodayFlag ? '✅ [Latest Update Today]' : 'ℹ️ [No updates today. Current progress summary]';

        return `${INTRO_ZH}\n\n${INTRO_EN}\n\n${BORDER}\n\n`
            + `📍 日文原文 / Japanese Original\n${results.map(r => r.orig).join('\n\n')}\n\n${BORDER}\n\n`
            + `📍 中文翻譯 / Chinese Translation\n${statusZH}\n${results.map(r => r.zh).join('\n\n')}\n\n${BORDER}\n\n`
            + `📍 English Translation\n${statusEN}\n${results.map(r => r.en).join('\n\n')}`;
    }

    // 翻譯並存入 GM storage（供 agent 頁讀取）
